package oop;
import java.io.*;
public class WIDETYPE_CASTING {
public static void main(String args[]) {
	int i =20;
	long l=i;
	double d=i;
	float f=i;
	System.out.println("Integer:"+i);
	System.out.println("long:"+l);
	System.out.println("double:"+d);
	System.out.println("float:"+f);
	
	
}
}
